# --- 4. ADK Agent Definitions (Supervisor Architecture) ---

# 4.1 Specialized Agents
AlertTriageAgent = LlmAgent(
    model=MODEL_NAME,
    name="TriageAgent",
    description="Specializes in analyzing the raw Alertmanager payload (labels and annotations) to determine the severity, component, and the initial probable cause.",
    instruction="You are an expert Alert Triage specialist. Your task is to extract the core problem and affected component from the raw alert data.",
    # No tools needed, just raw data analysis
)

KnowledgeRetrievalAgent = LlmAgent(
    model=MODEL_NAME,
    name="RetrievalAgent",
    description="Specializes in formulating knowledge retrieval queries and using the DocumentationRetrievalTool.",
    instruction="Analyze the Triage Agent's output. Formulate a precise question for the DocumentationRetrievalTool using the alert name and component to find the relevant runbook or fix.",
    tools=[DocumentationRetrievalTool],
)

ReportAnalysisAgent = LlmAgent(
    model=MODEL_NAME,
    name="ReportAgent",
    description="Specializes in synthesizing information from the TriageAgent and RetrievalAgent into a final, structured analysis report.",
    instruction=(
        "You are the final analyst. Combine the initial alert analysis with the retrieved knowledge. "
        "Your final output MUST be a JSON object conforming strictly to the AnalysisReport Pydantic schema."
    ),
    # Define the output schema for structured generation
    response_mime_type="application/json",
    response_schema=AnalysisReport.model_json_schema(),
)

# 4.2 Supervisor Agent (OpsSage) and Workflow
# The OpsSage agent is a SequentialAgent to ensure the flow is deterministic: Triage -> RAG -> Report.
sage_flow = SequentialAgent(
    name="sage",  # Root agent name
    description="The main OpsSage orchestrator. It executes a deterministic flow to analyze an alert.",
    sub_agents=[
        AlertTriageAgent,
        KnowledgeRetrievalAgent,
        ReportAnalysisAgent,
    ],
    # The final output of the ReportAnalysisAgent is what the SequentialAgent will return
    final_agent_name="ReportAgent",
)

# --- 5. FastAPI Application Setup ---


# Define the lifespan function to manage the ADK Runner lifecycle
@asynccontextmanager
async def lifespan(app: FastAPI):
    """Initializes the ADK Runner when the app starts and shuts it down when the app stops."""
    global runner
    try:
        # Initialize the ADK runner with the root agent
        runner = Runner(agent=sage_flow)
        logger.info("ADK Runner initialized with OpsSage Supervisor Agent.")
        yield
    except Exception as e:
        logger.error(f"Failed to initialize ADK Runner: {e}")
        # Allow the application to start even if ADK initialization fails, but mark it
        raise e


# Create the FastAPI app instance
app = FastAPI(
    title="OpsSage Multi-Agent System API",
    description="Endpoint for receiving Alertmanager webhooks and triggering the OpsSage ADK workflow.",
    version="1.0.0",
    lifespan=lifespan,
)


@app.post("/alert", response_model=AnalysisReport)
async def handle_alert(payload: AlertPayload) -> AnalysisReport:
    """
    Receives an Alertmanager webhook payload and triggers the OpsSage multi-agent workflow.
    """
    if not payload.alerts:
        raise HTTPException(
            status_code=400, detail="Payload must contain at least one alert."
        )

    # We process the first alert for simplicity in this example
    alert_info = payload.alerts[0]
    alert_name = alert_info.labels.get("alertname", "Unknown Alert")
    logger.info(f"Received alert: {alert_name} (Status: {alert_info.status})")

    # 1. Prepare the input for the ADK agent
    # The ADK state/context is a dictionary where we store input/output
    initial_state = {
        "alert_data": alert_info.model_dump(),
        "alert_payload_raw": json.dumps(alert_info.model_dump(), indent=2),
    }

    # 2. Run the ADK multi-agent flow
    try:
        # We start the agent run and pass the initial state
        final_event: Event = await runner.run_async(
            EventInput(
                author="AlertManager",
                actions=EventActions(message=f"New alert received: {alert_name}"),
                state=initial_state,
            )
        )

        # 3. Extract the structured report from the final agent's output
        if final_event.actions and final_event.actions.message:
            # The ReportAgent is configured to return a JSON string
            report_json_str = final_event.actions.message.strip()

            # The LLM output might have markdown formatting, strip it if necessary
            if report_json_str.startswith("```json") and report_json_str.endswith(
                "```"
            ):
                report_json_str = report_json_str[7:-3].strip()

            final_report = AnalysisReport.model_validate_json(report_json_str)

            # 4. Standard Interface Output: Return the structured report
            # This structured JSON is the 'standard interface' that can be consumed
            # and formatted by a Telegram/Slack/Email wrapper service.
            return final_report

        raise HTTPException(
            status_code=500, detail="Agent returned empty or unexpected response."
        )

    except Exception as e:
        logger.error(f"ADK Workflow Error: {e}", exc_info=True)
        # Raise a more descriptive error for the Alertmanager to potentially log
        raise HTTPException(
            status_code=500, detail=f"ADK processing failed: {type(e).__name__} - {e}"
        )


